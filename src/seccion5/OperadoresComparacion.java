package seccion5;

public class OperadoresComparacion {
    public static void main(String[] args) {
        System.out.println("Operadores de comparaciÃ³n en Java");
        int a=3, b=2;
        System.out.println("a= " + a + ", b= " + b);

        //Igualdad
        System.out.println("a == b: " + (a==b));

        //Desigualdad
        System.out.println("a != b: " + (a!=b));

        //Mayor que
        System.out.println("a > b: " + (a>b));

        //Menor que
        System.out.println("a < b: " + (a<b));

        //Mayor o igual que
        System.out.println("a >= b: " + (a>=b));

        //Menor o igual que
        System.out.println("a <= b: " + (a<=b));
    }
}
